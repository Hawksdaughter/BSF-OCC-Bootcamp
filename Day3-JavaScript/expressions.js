// String expression
console.log('Greetings, ' + 'all of the world!');

// Logical expression
let darkness = true;
let daytime = false;
console.log('Is it dark and daytime?', darkness && daytime);
console.log('Can it be dark OR the daytime?', darkness || daytime);
console.log('The REVERSE value of daytime is', !daytime);

// Assignment expression
let a = 20;
let b = 10;
a += b;
console.log(a); // 20 plus 10
a -= b;
console.log(a); // 30 minus 10
a *= b;
console.log(a); // 20 times 10
a /= b;
console.log(a); // 200 divided by 10
